/**
 * 常用数据类型
 */

public class Study04_Type {
    public static void main(String[] args) {
        int i = 10;             // 整数
        double pi = 3.14;       // 浮点数
        char word = 'a';       // 一个字符 单引号
        String words = "abc";   // 字符串 双引号
        System.out.println("整数:" + i + "字符:" + word + "浮点数:" + pi + "字符串:" + words);
    }
}
