/**
 * 数组 获取数组长度
 * length属性
 */

public class Study34_Array {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};
        System.out.println(arr.length);
        for (int j : arr) System.out.println(j);
        // for (int i = 0; i < arr.length; i++) System.out.println(arr[i]);
    }
}
