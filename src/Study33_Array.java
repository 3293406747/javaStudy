/**
 * 数组 分配空间并赋值
 */

public class Study33_Array {
    public static void main(String[] args) {
        // 声明 分配空间 赋值
        // int[] arr = new int[]{1,2,3,4,5};
        int[] arr = {1, 2, 3, 4, 5};    // 一行的时候可以省略 new int[] 非一行不能省
        // 不需要赋值就能读，读出来是默认值
        System.out.println(arr[0]);
        System.out.println(arr[1]);

    }
}
